CREATE OR ALTER PROCEDURE sp_NV_XemGiaTinhTien
    @MaSanPham CHAR(4)
AS
BEGIN
    SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED; 
    
    BEGIN TRANSACTION;
    BEGIN TRY
        SELECT TenSanPham, GiaBan 
        FROM Sanpham 
        WHERE MaSanPham = @MaSanPham;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END;
GO

